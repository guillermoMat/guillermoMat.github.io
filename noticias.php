                el inicio primaveral, porque fue un 21 de septiembre el día en 
                que fueron repatriados los restos de Sarmiento, que fue él mismo 
                un gran estudiante.
               En nuestro país, la fecha fue sugerida por el entonces estudiante 
                Salvador Debenedetti, que con el tiempo se convertiría en un 
                importante arqueólogo. En 1902, Debenedetti era el presidente del 
                centro de estudiantes de la Facultad de Filosofía y Letras de la 
                Universidad de Buenos Aires, y en ese carácter sugirió a las 
                autoridades la celebración, el 21 de septiembre, del día del 
                estudiante.
               La propuesta, en principio aceptada por la centro educativo, se fue 
                extendiendo posteriormente a otros ámbitos educativos, y en la actualidad
                se celebra en todo el país. En esta jornada la juventud es protagonista 
                con actividades de todo tipo, en su mayoría al aire libre como en complicidad  
                con la estación que se inicia. Históricamente, a los estudiantes se les han 
