            <h2><u>5 Claves para la innovación educativa</u></h2>
            <p>1. La calidad del profesorado es crucial para la 
                innovación educativa. Se requiere de docentes con 
                sensibilidad, compromiso, constancia, creatividad,
                empatía y capacidad para motivar y emocionar a sus
                estudiantes. Docentes con apertura, autocrítica y
                flexibilidad para permanentemente reinventarse y
                transformar sus prácticas de enseñanza acorde a 
                las necesidades específicas de cada grupo de estudiantes.
            </p>
            <p>2. La innovación es posible y se hace sostenible si 
                ocurren cambios en la gestión escolar. Se necesita 
                de un liderazgo pedagógico que sea inclusivo, distribuido 
                y no adultocéntrico, capaz de generar y mantener una 
                cultura de innovación en el centro educativo. Un entorno 
                colaborativo es fundamental para la innovación educativa.
            </p>
            <p>3. La innovación es el recurso indispensable para mejorar de forma 
                efectiva la educación y la misma surge de la revisión crítica
                del interior de la propia escuela. Innovar implica una transformación 
                radical del modelo educativo. Es necesario desaprender el modelo centrado 
                en la información, la memorización y la verticalidad, y construir modelos 
                centrados en metodologías activas, el aprender a aprender, la autorregulación
                 del aprendizaje y el trabajo colaborativo y cooperativo
            </p>
            <p>4. La diversidad es otro pilar fundamental para la innovación y 
                una oportunidad para los aprendizajes. “Los grupos diversos son 
                más creativos y capaces de innovar”,
            </p>
